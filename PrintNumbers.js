//Printing numbers from 1 to 10
console.log("Printing Numbers from 1 to 10")
for(var i=1; i<=10; i++)
{
	console.log(i + " ")
}
console.log("\n")


//Printing Odd numbers from 1 to 10
console.log("Printing Odd Numbers from 1 to 10")
for(var i=1; i<=10; i++)
{
	if(i%2 != 0)
	console.log(i + " ")
}
console.log("\n")


//Printing Even numbers from 1 to 10
console.log("Printing Even Numbers from 1 to 10")
for(var i=1; i<=10; i++)
{
	if(i%2 == 0)
	console.log(i + " ")
}
console.log("\n")